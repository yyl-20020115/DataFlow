PDF2MODEL_TRAIN_BACKEND_BASE = "base"

# 可供后续可能的dataflex系列后端接入
PDF2MODEL_TRAIN_BACKEND_DATAFLEX_LESS = "dataflex-less"

DATAFLEX_BACKEND_SPECS = {
    PDF2MODEL_TRAIN_BACKEND_DATAFLEX_LESS: {
        "component_name": "less",
        "suffix": "less",
    },
}


def _get_dataflex_backend_spec(pdf2model_train_backend: str):
    return DATAFLEX_BACKEND_SPECS.get(pdf2model_train_backend)


def _assert_pdf2model_train_backend_allowed(qa_type: str, pdf2model_train_backend: str) -> None:
    if pdf2model_train_backend.startswith("dataflex-"):
        if qa_type != "kbc":
            raise AssertionError(
                f"pdf2model_train_backend={pdf2model_train_backend} is only valid when qa=kbc, got qa={qa_type!r}"
            )
        if _get_dataflex_backend_spec(pdf2model_train_backend) is None:
            supported_backends = ", ".join(sorted(DATAFLEX_BACKEND_SPECS.keys()))
            raise AssertionError(
                f"Unsupported pdf2model_train_backend={pdf2model_train_backend!r}. "
                f"Supported dataflex backends: {supported_backends or '(none)'}"
            )
    if qa_type == "vqa" and pdf2model_train_backend != PDF2MODEL_TRAIN_BACKEND_BASE:
        raise AssertionError(f"vqa only supports pdf2model_train_backend={PDF2MODEL_TRAIN_BACKEND_BASE!r}, got {pdf2model_train_backend!r}")


def _path_for_yaml(cwd: Path, target: Path) -> str:
    """Prefer path relative to cwd for yaml portability."""
    try:
        return str(target.resolve().relative_to(cwd.resolve()))
    except ValueError:
        return str(target.resolve())


def _build_dataflex_components_yaml_text(selector_cache_dir: str, component_name: str, backend_name: str) -> str:
    return f"""# Auto-generated by dataflow pdf2model init ({backend_name}).
selectors:
  {component_name}:
    name: {component_name}
    params:
      cache_dir: {selector_cache_dir}
      gradient_type: adam
      proj_dim: 4096
      seed: 123
      save_interval: 16
"""


